    fun toQuery(after: LocalDateTime? = null, before: LocalDateTime? = null): List<String> {
      val queryList = mutableListOf<String>()
      val formatter = DateTimeFormatter.ofPattern("yyyy/MM/dd")

      var dateQuery = ""
      if (after != null) {
        dateQuery += "after:${after.format(formatter)}"
      } else if (before != null) {
        dateQuery += "before:${before.format(formatter)}"
      }
      var senderQuery = ""
      senders.forEachIndexed { index, sender ->
        senderQuery += "from:${sender.email} "
        if (index == senders.size - 1) {
          queryList.add("&q={$dateQuery $senderQuery}")
        } else if (index % 100 == 0) {
          queryList.add("&q={$dateQuery $senderQuery")
          senderQuery = ""
        }
      }

      return queryList
    }

